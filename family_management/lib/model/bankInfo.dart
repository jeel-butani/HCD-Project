class BankInfo {
  String bankName;
  String branchName;
  String ifscCode;
  String ownerName;
  String accountName;
  String email;
  String phoneNumber;
  String accountType;

  BankInfo({
    required this.bankName,
    required this.branchName,
    required this.ifscCode,
    required this.ownerName,
    required this.accountName,
    required this.email,
    required this.phoneNumber,
    required this.accountType,
  });
}
